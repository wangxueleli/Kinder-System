package bean;

public class Homework {
	private Integer id;
	private String bigdec;
	private String imgurl;
	private Integer wx;
	private String timeing;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getBigdec() {
		return bigdec;
	}
	public void setBigdec(String bigdec) {
		this.bigdec = bigdec;
	}
	public String getImgurl() {
		return imgurl;
	}
	public void setImgurl(String imgurl) {
		this.imgurl = imgurl;
	}
	public Integer getWx() {
		return wx;
	}
	public void setWx(Integer wx) {
		this.wx = wx;
	}
	public String getTimeing() {
		return timeing;
	}
	public void setTimeing(String timeing) {
		this.timeing = timeing;
	}
	
}
