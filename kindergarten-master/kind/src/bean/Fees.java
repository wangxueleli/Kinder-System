package bean;

public class Fees {
	private Integer id;
	private String feesort;
	private String fatherclass;
	private String money;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getFeesort() {
		return feesort;
	}
	public void setFeesort(String feesort) {
		this.feesort = feesort;
	}
	public String getFatherclass() {
		return fatherclass;
	}
	public void setFatherclass(String fatherclass) {
		this.fatherclass = fatherclass;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}

}
